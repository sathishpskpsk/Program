package psk;

public class app2 {

	public static void main(String[] args) {
		String a = "sk";
		String b = "ss";
		String r = a + b;
		System.out.println("Result: " + r);
		// int
		int q = 10;
		int w = 20;
		int re = q + w;
		System.out.println("Result: " + re);
		// double
		double e = 23.432D;
		double t = 33.45D;
		double res = e + t;
		System.out.println("Result: " + res);
		// char
		char aa = 'a';
		char bb = 'b';
		int resu = (aa + bb);
		System.out.println("Result: " + resu);

		int m = 10;
		int n = 20;
		// 10 + 20 = 30
		int ans = m-- * n--;
		System.out.println("Ans:" + ans);

		int answ = m;
		System.out.println(answ);

	}
}
