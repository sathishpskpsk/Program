package psk;

public class C extends B {
	private int e=1000;
	private int f=2000;
	public C() {
		super();
	}
	public C(int e, int f) {
		super();
		this.e = e;
		this.f = f;
	}
	public int getE() {
		return e;
	}
	public void setE(int e) {
		this.e = e;
	}
	public int getF() {
		return f;
	}
	public void setF(int f) {
		this.f = f;
	}
	

}
